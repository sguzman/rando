    fn assert_approx_eq(a: f64, b: f64, tol: f64, msg: &str) {
        let diff = (a - b).abs();
        let rel_err = if b != 0.0 { diff / b.abs() } else { diff };
        assert!(rel_err < tol || diff < tol, "{} ({} vs {}, err={})", msg, a, b, rel_err);
    }

